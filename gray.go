package imgconv

import "image"

func ToGray(img image.Image) image.Image {
	if img, ok := img.(*image.Gray); ok {
		return img
	}
	bounds := img.Bounds()
	gray := image.NewGray(bounds)
	for y := bounds.Min.Y; y < bounds.Max.Y; y++ {
		for x := bounds.Min.X; x < bounds.Max.X; x++ {
			gray.Set(x, y, img.At(x, y))
		}
	}
	return gray
}
